# basics.py

# This program demonstrates basic data types in Python

# String
name = "Alice"
print("Name:", name)

# Integer
age = 30
print("Age:", age)

# Float
height = 5.6
print("Height:", height)

# List
hobbies = ["reading", "cycling", "hiking"]
print("Hobbies:", hobbies)

# Tuple
coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

# Dictionary
person = {
    "name": "Alice",
    "age": 30,
    "height": 5.6
}
print("Person Dictionary:", person)

